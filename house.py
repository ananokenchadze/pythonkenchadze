from turtle import *
# we want to draw a house
# step1 - we have to draw a square
width(5)
color("purple")
begin_fill()
forward(200)
left(90)

forward(200)
left(90)

forward(200)
left(90)

forward(200)
left(90)
end_fill()
# we finished drawing a square

# let's draw a door

forward(70)
